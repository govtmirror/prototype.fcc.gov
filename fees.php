                      <p class="list-item-text">for licenses, equipment approvals, antenna registrations, tariff filings, formal complaints (not ordinary complaints), and other authorizations and regulatory actions.</p>
                    </li>
                    <li class="block-list-item">
                      <h3 class="list-item-heading"><a href="fees.php#">Annual Regulatory Fees</a></h3>
                      <p class="list-item-text">collected from specific categories of regulated entities in the mass media, common carrier, wireless, international and cable television services.</p>
                    </li>
                    <li class="block-list-item">
                      <h3 class="list-item-heading"><a href="fees.php#">Freedom of Information Act (FOIA) Fees</a></h3>
                      <p class="list-item-text">for processing requests under the Freedom of Information Act.</p>
                    </li>
                  </ul>
                </div>
                <div class="col-sm-6 col-xs-12">
                  <ul class="block-list block-list-standard list-types-of-fees">
                    <li class="block-list-item">
                      <h3 class="list-item-heading"><a href="fees.php#">Auction Payments</a></h3>
                      <p class="list-item-text">for upfront payments, down payments, and subsequent payments for licenses that the FCC auctions.</p>
                    </li>
                    <li class="block-list-item">
                      <h3 class="list-item-heading"><a href="fees.php#">Annual Regulatory Fees</a></h3>
                      <p class="list-item-text">collected from specific categories of regulated entities in the mass media, common carrier, wireless, international and cable television services.</p>
                    </li>
                    <li class="block-list-item">
                      <h3 class="list-item-heading"><a href="fees.php#">Forfeitures</a></h3>
                      <p class="list-item-text">are penalties that the FCC may assess for violations of law or noncompliance with authorizations.</p>
                    </li>
