              <h1 class="page-title">Fees Overview</h1>
            </div>
            <div class="block-content">
              <p>The FCC collects regulatory fees and processing fees. Congress authorizes our authority to impose and collect such fees. Application processing fees are deposited in the U.S. Treasury and are not available to the FCC.</p>
